package com.cqu.entity;

public class Reader {
	
	private String reader_id;	//读者编号
	private String reader_name;	//读者姓名
	private String reader_college; //读者学院
	private String reader_phone; //读者电话
	private String reader_pwd; // 登录密码
	
	public String getReader_pwd() {
		return reader_pwd;
	}
	public void setReader_pwd(String reader_pwd) {
		this.reader_pwd = reader_pwd;
	}
	public String getReader_id() {
		return reader_id;
	}
	public void setReader_id(String reader_id) {
		this.reader_id = reader_id;
	}
	public String getReader_name() {
		return reader_name;
	}
	public void setReader_name(String reader_name) {
		this.reader_name = reader_name;
	}
	public String getReader_college() {
		return reader_college;
	}
	public void setReader_college(String reader_college) {
		this.reader_college = reader_college;
	}
	public String getReader_phone() {
		return reader_phone;
	}
	public void setReader_phone(String reader_phone) {
		this.reader_phone = reader_phone;
	}
	

	

}
